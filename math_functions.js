// ---------- Math.PI
console.log('Math.PI : ', Math.PI); // 3.141592653589793

// ---------- Math.round(number)
console.log('Math.round(3.6) : ', Math.round(3.6)); // 4
console.log('Math.round(3.3) : ', Math.round(3.3)); // 3

// ---------- Math.ceil(number)
console.log('Math.ceil(3.6) : ', Math.ceil(3.6)); // 4
console.log('Math.ceil(3.3) : ', Math.ceil(3.3)); // 4
console.log('Math.ceil(-3.3) : ', Math.ceil(-3.3)); // -3
console.log('Math.ceil(-3.6) : ', Math.ceil(-3.6)); // -3

// ---------- Math.floor(number)
console.log('Math.floor(3.3) : ', Math.floor(3.3)); // 3
console.log('Math.floor(3.6) : ', Math.floor(3.6)); // 3
console.log('Math.floor(-3.3) : ', Math.floor(-3.3)); // -4
console.log('Math.floor(-3.6) : ', Math.floor(-3.6)); // -4

// ---------- Math.trunc(string)
console.log("Math.trunc('9.6') : ", Math.trunc('9.6')); // 9
console.log("Math.trunc('abc') : ", Math.trunc('abc')); // NaN

// ---------- Math.pow(x, y)
console.log('Math.pow(2, 6) : ', Math.pow(2, 6)); // 64

// ---------- Math.sqrt(x)
console.log('Math.sqrt(25) : ', Math.sqrt(25)); // 5

// ---------- Math.abs(number)
console.log('Math.abs(-6) : ', Math.abs(-6)); // 6

// ---------- Math.random()
console.log(Math.random()); // 0.860682549120102

// ---------- Math.max(array)
console.log(Math.max(5, 9, 2, 7, 3, 1, 15)); // 15

// ---------- Math.min(array)
console.log(Math.min(5, 9, 2, 7, 3, 1, 15)); // 1
